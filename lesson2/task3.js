var a = Math.floor(Math.random() * 100 - 50); // рандомное число в диапазоне [-50; 50]
var b = Math.floor(Math.random() * 100 - 50);

if (a >= 0 && b >= 0) {
    alert('а равно ' + a + ', b равно ' + b + ', их разность равна ' + (a - b));
} else if (a < 0 && b < 0) {
    alert('а равно ' + a + ', b равно ' + b + ', их произведение равно ' + (a * b));
} else if ((a < 0 && b >= 0) || (a >= 0 && b < 0)) {
    alert('а равно ' + a + ', b равно ' + b + ', их сумма равна ' + (a + b));
}